"""ImageNet dataset."""

import numpy as np
from .imagenet_style import ImageNetStyle

# Per-channel mean and SD values in BGR order
_MEAN = [0.406, 0.456, 0.485]
_SD = [0.225, 0.224, 0.229]

# Eig vals and vecs of the cov mat
_EIG_VALS = np.array([[0.2175, 0.0188, 0.0045]])
_EIG_VECS = np.array(
    [[-0.5675, 0.7192, 0.4009], [-0.5808, -0.0045, -0.8140], [-0.5836, -0.6948, 0.4203]]
)


class ImageNet(ImageNetStyle):
    """ImageNet dataset."""

    def __init__(self, *args, **kwargs):
        super().__init__("ImageNet", [_MEAN, _SD, _EIG_VALS, _EIG_VECS], *args, **kwargs)

